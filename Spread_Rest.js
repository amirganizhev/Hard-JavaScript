/*Spread оператор позволяет развернуть готовый массив в другом массиве.
Пример ниже:*/
const name = ['a', 'm', 'i', 'r'];
const surname = ['g', 'a', 'n', 'i', 'z', 'h', 'e', 'v'];

const fio = [...name, 'ginardovich', ...surname]
/*Выведет "amir ginardovich ganizhev"*/
console.log(fio)

